private void showTable()
    {
    int c;
    try{
           
           PreparedStatement pst =new ConnectDB().getCon().prepareStatement("select * from Product");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData Rss= rs.getMetaData();
            c= Rss.getColumnCount();
            DefaultTableModel Df= (DefaultTableModel)jTable3.getModel();
            Df.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2=new Vector();
                for(int a=1;a<=c;a++)
                {
                    v2.add(rs.getInt(1));
                    v2.add(rs.getString(2));
                    v2.add(rs.getString(3));
                    v2.add(rs.getFloat(4));
                    v2.add(rs.getString(5));
                }
                Df.addRow(v2);
            }
            rs.close();
        }catch(SQLException ex){}
        
    }

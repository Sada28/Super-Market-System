    private void rSMaterialButtonRectangle4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangle4ActionPerformed
        Product prod=new Product();
        prod.setProdName(txtProdName.getText());
        prod.setProdDesc(txtProdDesc.getText());
        prod.setProdPrice(Float.parseFloat(txtProdPrice.getText()));
        prod.setCateName((String)cmbCategory.getSelectedItem());
        prod.setStockQty(Integer.parseInt(txtStockQty.getText()));
        
        
        if(cmbCategory.getSelectedItem().equals("")){
            cmbCategory.requestFocus();
            JOptionPane.showMessageDialog(this,"Please Enter Select Category","SMS-Products",2);
        }
        
        if(txtProdName.getText().equalsIgnoreCase("")){
            txtProdName.requestFocus();
            JOptionPane.showMessageDialog(this,"Please Enter Product Name","SMS-Products",2);
        }
        
        else if(txtProdDesc.getText().equalsIgnoreCase("")){
            txtProdDesc.requestFocus();
            JOptionPane.showMessageDialog(this,"Please Enter Product Description","SMS-Products",2);
        }
        else if(txtProdPrice.getText().equalsIgnoreCase("")){
            txtProdPrice.requestFocus();
            JOptionPane.showMessageDialog(this,"Please Enter Product Price","SMS-Products",2);
        } 
        else if(txtStockQty.getText().equalsIgnoreCase("")){
            txtStockQty.requestFocus();
            JOptionPane.showMessageDialog(this,"Please Enter Product Stock Qty","SMS-Products",2);
        } 
   
         try{
            PreparedStatement pr=new ConnectDB().getCon().prepareStatement("INSERT INTO `product`(`PROD_NAME`, `PROD_DESCRIPTION`, `PROD_PRICE`, `CATE_NAME`) VALUES (?,?,?,?)");
            pr.setString(1,prod.getProdName());
            pr.setString(2,prod.getProdDesc());
            pr.setFloat(3,prod.getProdPrice());
            pr.setString(4,prod.getCateName());
            pr.executeUpdate();
            
            prod.setProdId(txtProdName.getText());
            PreparedStatement prr=new ConnectDB().getCon().prepareStatement("INSERT INTO `stock`(`PROD_ID`, `PROD_NAME`, `CATE_NAME`, `STOCK_QTY`, `REMAINING_STOCK`) VALUES (?,?,?,?,?)");
            prr.setInt(1,prod.getProdId());
            prr.setString(2,prod.getProdName());
            prr.setString(3,prod.getCateName());
            prr.setInt(4,prod.getStockQty());
            prr.setInt(5,prod.getStockQty());        
            prr.executeUpdate();
            prr.close();
            JOptionPane.showMessageDialog(this,"Product Added","SMS- Admin Products",1);
            pr.close();
            txtProdName.setText("");
            txtProdDesc.setText("");
            txtProdPrice.setText("");
            txtStockQty.setText("");
            cmbCategory.removeAllItems();
         }
       catch(SQLException e){
           System.out.println(e);
       }
        
    }//GEN-LAST:event_rSMaterialButtonRectangle4ActionPerformed


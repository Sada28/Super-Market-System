    private void rSMaterialButtonRectangle2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangle2MouseClicked
        int n=JOptionPane.showConfirmDialog(this, "Do you To place this order","SMS-Order",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
        if(n==JOptionPane.YES_OPTION){
            PreparedStatement pr;
            PlaceOrder po=new PlaceOrder();
            po.setCustID();
            
            
            // Create Payment
            String utr=JOptionPane.showInputDialog(this,"please make payment on this 'supermarket9090@apl' UPI ID and Enter Payment UTR Number","SMS-Order",JOptionPane.INFORMATION_MESSAGE);
            try {
                pr = new ConnectDB().getCon().prepareStatement("INSERT INTO `payment_trans`(`pay_utr`, `cust_id`) VALUES (?,?)");
                pr.setString(1, utr);
                pr.setInt(2, po.getCustID());
                pr.executeUpdate();
                pr.close();
            } catch (SQLException ex) {}
            
            //create order
            try {
                pr = new ConnectDB().getCon().prepareStatement("INSERT INTO `order_product`(`CUST_ID`) VALUES (?)");
                pr.setInt(1,po.getCustID());
                pr.executeUpdate();
                pr.close();
            } catch (SQLException ex) {}
           
            po.setOrderID();
            //Transaction 
            try {
                //INSERT INTO `order_trans`(`order_id`, `prod_id`, `status`, `order_date`, `prod_qty`) VALUES(?,?,?,?,?)
                pr = new ConnectDB().getCon().prepareStatement("INSERT INTO `order_trans`(`order_id`, `prod_id`, `status`, `order_date`, `prod_qty`) VALUES(?,?,?,?,?)");
                for(int i=0; i< po.list.size();i++){
                    pr.setInt(1, po.getOrderID());
                    pr.setInt(2, po.list.get(i));
                    pr.setBoolean(3,false);
                    pr.setDate(4, new java.sql.Date(new java.util.Date().getTime()));
                    pr.setInt(5,po.listQty.get(i));
                    pr.executeUpdate();
                }
                pr.close();
            } catch (SQLException ex) {}
            JOptionPane.showMessageDialog(this, "Order Placed Successfully");
            
            // Minus stock
            try{
                //
                ResultSet rs =new ConnectDB().getCon().prepareStatement("SELECT `PROD_NAME`,`PROD_QTY` FROM `cart`").executeQuery();
                while(rs.next()){                    
                    PreparedStatement prr=new ConnectDB().getCon().prepareStatement("SELECT `REMAINING_STOCK` FROM `stock` WHERE prod_name=?");
                    prr.setString(1, rs.getString(1));
                    ResultSet rss=prr.executeQuery();
                    while(rss.next()){
                        // Update Stock
                        PreparedStatement p=new ConnectDB().getCon().prepareStatement("UPDATE `stock` SET `REMAINING_STOCK`=? where prod_name=?");
                        // REMAINING_STOCK - PROD_QTY
                        p.setInt(1, rss.getInt(1)- rs.getInt(2));
                        p.setString(2, rs.getString(1));
                        p.executeUpdate();
                        p.close();
                    }
                    rss.close();
                    prr.close();
    
                }
                
                rs.close(); 
            }
            catch(SQLException e){}
            
            new Bill().setVisible(true);
            
        }
           
           
    }//GEN-LAST:event_rSMaterialButtonRectangle2MouseClicked


    private void showTable(){
    int c;
        try{
           
           PreparedStatement pst =new ConnectDB().getCon().prepareStatement("SELECT `PROD_ID`, `PROD_NAME`, `PROD_DESC`, `CATE_NAME`, `PROD_PRICE`,`PROD_QTY` FROM `cart`");
           ResultSet rs = pst.executeQuery();
            ResultSetMetaData Rss= rs.getMetaData();
            c= Rss.getColumnCount();
            DefaultTableModel Df= (DefaultTableModel)jTable1.getModel();
            Df.setRowCount(0);
            float val=0;
            
            while(rs.next())
            {
                Vector v2=new Vector();
                for(int a=1;a<=c;a++)
                {
                    v2.add(rs.getInt(1));
                    v2.add(rs.getString(2));
                    v2.add(rs.getString(3));
                    v2.add(rs.getString(4));
                    v2.add(rs.getFloat(5));
                    v2.add(rs.getInt(6));
                    v2.add(rs.getInt(5)*rs.getInt(6));
                }
                val+=rs.getFloat(5)*rs.getInt(6);
                Df.addRow(v2);
            }
            
            jLabel12.setText(String.valueOf(val));
            rs.close();
        }catch(SQLException ex){}
}

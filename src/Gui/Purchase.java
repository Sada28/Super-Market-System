    private void rSMaterialButtonRectangle1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangle1MouseClicked
        // Add to cart
        cart.setProdQTY(Integer.parseInt(JOptionPane.showInputDialog("Enter Quantity...")));
        
        PreparedStatement pr;
        try {
            pr = new ConnectDB().getCon().prepareStatement("INSERT INTO `cart`(`PROD_ID`, `PROD_NAME`, `PROD_DESC`, `PROD_PRICE`, `CATE_NAME`, `CUST_ID`,`PROD_QTY`) VALUES (?,?,?,?,?,?,?)");
            pr.setInt(1, cart.getProdId());
            pr.setString(2,cart.getProdName());
            pr.setString(3, cart.getProdDesc());
            pr.setFloat(4, cart.getProdPrice());
            pr.setString(5, cart.getCateName());
            pr.setInt(6, cart.getCust_id());
            pr.setInt(7, cart.getProdQTY());
            pr.executeUpdate();
            JOptionPane.showMessageDialog(this,"Product Added to cart");
        } catch (SQLException ex) {    }
        
    }//GEN-LAST:event_rSMaterialButtonRectangle1MouseClicked

